# Строки
# Исключения
# Модули и их применение
# Простейшие методы строк пишутся через точку

string = 'Язык программирования Python'
print(string.lower())  # вывод только строчных символов
print(string.upper())  # вывод только заглавных символов
print(string.capitalize())  # вывод символов с первым большим символом
print(string.title())  # вывод всех слов в предложении с большой буквы, аббривиатуры


